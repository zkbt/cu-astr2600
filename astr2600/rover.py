import numpy as np
from turtle import Turtle, bye

xsize = 500
ysize = 500
width = 3
speed = 6

# how far away could we still identify life?
diggingradius = 50

np.random.seed(0)
xlife = int(np.random.uniform(-xsize/2, xsize/2))
ylife = int(np.random.uniform(-ysize/2, ysize/2))

class Rover(Turtle):

    def __init__(self, shape='square', **kw):
        '''
        Initialize a science rover object. It can do
        everything a Python turtle can do, plus science!
        '''

        Turtle.__init__(self, shape=shape, **kw)

        self.speed(speed)
        self.width(width)
        self.screen.bgcolor('lightcoral')
        self.screen.setup(xsize, ysize)

    def bye(self):
        bye()

    def reset(self):
        Turtle.reset(self)
        self.speed(speed)
        self.width(width)

    def wander(self, distance=diggingradius):
        '''
        Ask the rover to wander in a random direction.

        Parameters
        ----------
        distance : float
            How far the rover should wander.
        '''
        self.left(np.random.uniform(-180, 180))

        x, y = self.pos()
        if x >= xsize/2 or x <= -xsize/2 or y >= ysize/2 or y <= -ysize/2:
            self.setheading(self.towards(0,0))
        self.forward(distance)

    def doscience(self):
        '''
        Ask the rover to do a little science experiment at its current location.
        Maybe it will find life. Maybe not!

        Returns
        -------
        life : bool
            True if life has been found.
            False if life hasn't been found.
        '''



        foundlife = self.distance(xlife, ylife) < diggingradius
        if foundlife:
            print("Hooray! You found life on Mars! It's at {}.".format((xlife, ylife)))

            ncircles = 5
            self.goto(xlife, ylife)
            self.dot(2*diggingradius, 'mediumseagreen')

            for i in range(ncircles):
                self.left(360/ncircles)
                self.circle(40)
        else:
            # draw a dot to record where science has happened
            self.dot(2*diggingradius, 'lightgray')
            x, y = self.pos()
            print("No life at {:>11}. Keep looking!".format(str((int(x), int(y)))))

        return foundlife

